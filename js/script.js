window.onscroll = () =>{

  if (window.scrollY >= dishe.offsetTop - 400) {
    dishesBox.style.transform = "scale(1)";
  }
  if (window.scrollY >= about.offsetTop - 400) {
    aboutImg.style.transform = "translateX(0)";
    aboutContent.style.transform = "translateX(0)";
    aboutImg.style.opacity = "1";
    aboutContent.style.opacity = "1";
  }
  if (window.scrollY >= menus.offsetTop - 400) {
    menuBox.style.transform = "scale(1)";
  }
  if (window.scrollY >= footer.offsetTop - 500) {
    footerCredit.style.transform = "translateX(0)";
  }

  menu.classList.remove('fa-times');
  navbar.classList.remove('active');

  section.forEach(sec =>{

    let top = window.scrollY;
    let height = sec.offsetHeight;
    let offset = sec.offsetTop - 150;
    let id = sec.getAttribute('id');

    if(top >= offset && top < offset + height){
      navLinks.forEach(links =>{
        links.classList.remove('active');
        document.querySelector('header .navbar a[href*='+id+']').classList.add('active');
      });
    };

  });

}

